
package com.doublechaintech.uxs;


public class UxsRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public UxsRuntimeException() {
        super();
    }


    public UxsRuntimeException(String message) {
        super(message);
    }


    public UxsRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public UxsRuntimeException(Throwable cause) {
        super(cause);
    }


   
}












